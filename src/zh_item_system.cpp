// ==================== 6. 属性计算 (已做性能优化) ====================
// 优化说明：为防止每帧多次遍历背包，将背包被动道具判定合并为一次遍历

int get_total_atk() {
    int total_atk = zh_player.atk;
    
    // 1. 武器攻击力
    if (zh_player.eq_weapon != -1) {
        total_atk += get_item_by_id(zh_player.eq_weapon).value;
    }
    
    // 2. 套装加成
    if (has_sailor_set())  total_atk += 10;
    if (has_phantom_set()) total_atk += 80;   // 幻影套装强力加攻
    if (has_dragon_set())  total_atk += 200;  // 龙血套装毁灭攻击
    
    // 3. 背包特殊被动道具加成 (单次遍历背包提高性能)
    for (int i = 0; i < MAX_INVENTORY_SIZE; i++) {
        int item_id = zh_player.inventory[i];
        if (item_id == -1) continue;
        
        if (item_id == 208) total_atk += 30;  // 大马士革陨铁
        if (item_id == 222) total_atk += 50;  // 冰龙的吐息
        if (item_id == 240) total_atk += 100; // 霸王之证
        if (item_id == 247) total_atk += 25;  // 克拉肯的墨汁
    }
    
    return total_atk;
}

int get_total_def() {
    int total_def = zh_player.def;
    
    // 1. 防具防御力
    int eq_ids[] = {zh_player.eq_head, zh_player.eq_chest, zh_player.eq_legs, zh_player.eq_shoes};
    for (int j = 0; j < 4; j++) {
        if (eq_ids[j] != -1) {
            total_def += get_item_by_id(eq_ids[j]).value;
        }
    }
    
    // 2. 套装加成
    if (has_sailor_set())  total_def += 20;
    if (has_steel_set())   total_def += 15;
    if (has_earth_set())   total_def += 150;  // 大地守护套装极致防御
    if (has_phantom_set()) total_def += 40;
    if (has_dragon_set())  total_def += 120;
    
    // 3. 背包特殊被动道具加成 (单次遍历背包提高性能)
    for (int i = 0; i < MAX_INVENTORY_SIZE; i++) {
        int item_id = zh_player.inventory[i];
        if (item_id == -1) continue;
        
        if (item_id == 205) total_def += 40;  // 海妖的鳞片
        if (item_id == 211) total_def += 60;  // 龙骨化石
        if (item_id == 248) total_def += 150; // 利维坦的鳞片
    }
    
    return total_def;
}