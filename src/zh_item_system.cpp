ZH_Item get_item_by_id(int id) {
    if (id < 10000) {
        for(int i = 0; i < zh_data_fixed_items_count; i++) {
            if(zh_data_fixed_items[i].id == id) return zh_data_fixed_items[i];
        }
        return {-1, "未知物品", 0, 0, 0};
    } else {
        int slot = (id / 1000) % 10 - 1;
        int affix_idx = (id / 100) % 10;
        int base_idx = id % 100;
        if (slot < 0 || slot > 4) slot = 0;
        if (affix_idx < 0 || affix_idx > 9) affix_idx = 0;
        if (base_idx < 0 || base_idx > 9) base_idx = 0;
        
        static ZH_Item dyn_item;
        dyn_item.id = id;
        dyn_item.type = slot + 1; 
        
        static char name_bufs[10][64];
        static int buf_idx = 0;
        buf_idx = (buf_idx + 1) % 10;
        snprintf(name_bufs[buf_idx], 64, "%s%s", zh_dyn_affixes[affix_idx], zh_dyn_bases[slot][base_idx]);
        dyn_item.name = name_bufs[buf_idx];
        
        dyn_item.value = zh_dyn_base_values[slot][base_idx] + zh_dyn_affix_bonus[affix_idx];
        if(dyn_item.value < 1) dyn_item.value = 1;
        dyn_item.price = dyn_item.value * 25 + (affix_idx * 150); 
        
        return dyn_item;
    }
}

// ==================== 4. 优化后的掉落系统 ====================
int get_random_drop_id() {
    int r = rand() % 100;
    
    if(r < 40) { 
        // 40% 掉落随机属性散件装备
        int drop_slot = rand() % 5 + 1;
        int drop_affix = rand() % 10;
        int drop_base = rand() % 10;
        return 10000 + (drop_slot * 1000) + (drop_affix * 100) + drop_base;
        
    } else if(r < 55) { 
        // 15% 掉落怪物的杂碎材料(皮、爪、牙等，用于锻造散件或卖钱)
        return 421 + rand() % 15;
        
    } else if(r < 65) { 
        // 10% 掉落矿石(各种纯度，用于锻造主体)
        return 401 + rand() % 10;
        
    } else if(r < 75) {
        // 10% 掉落草药和回复药剂
        int herbs[] = {101, 102, 108, 109, 411, 412, 413, 414, 415, 416, 417, 418};
        return herbs[rand() % 12];
        
    } else if(r < 80) {
        // 5% 极低概率掉落世界BOSS的稀有核心(用于锻造神装)
        return 441 + rand() % 5;
        
    } else if(r < 90) {
        // 10% 掉落战斗内专属的爆炸物和法术卷轴
        return 301 + rand() % 50;
        
    } else { 
        // 10% 掉落固定的史诗装备或被动宝物 (避开手工锻造的装备 ID:500-647)
        int idx;
        do {
            idx = rand() % zh_data_fixed_items_count;
        } while (zh_data_fixed_items[idx].id >= 500 && zh_data_fixed_items[idx].id < 700);
        return zh_data_fixed_items[idx].id;
    }
}

// ==================== 5. 背包与套装加成判定 ====================
bool has_item_in_bag(int item_id) {
    for (int i = 0; i < 50; i++) {
        if (zh_player.inventory[i] == item_id) return true;
    }
    return false;
}

bool add_item_to_bag(int item_id) {
    for(int i = 0; i < 50; i++) {
        if(zh_player.inventory[i] == -1) {
            zh_player.inventory[i] = item_id;
            return true;
        }
    }
    return false;
}

// 基础套装判定
bool has_sailor_set() { return (zh_player.eq_head == 1 && zh_player.eq_chest == 2 && zh_player.eq_legs == 3 && zh_player.eq_shoes == 4); }
bool has_steel_set() { return (zh_player.eq_head == 16 && zh_player.eq_legs == 18); }

// 新增：三大锻造专属套装判定
bool has_earth_set() { return (zh_player.eq_head == 501 && zh_player.eq_chest == 502 && zh_player.eq_legs == 503 && zh_player.eq_shoes == 504); }
bool has_phantom_set() { return (zh_player.eq_head == 506 && zh_player.eq_chest == 507 && zh_player.eq_legs == 508 && zh_player.eq_shoes == 509); }
bool has_dragon_set() { return (zh_player.eq_head == 511 && zh_player.eq_chest == 512 && zh_player.eq_legs == 513 && zh_player.eq_shoes == 514); }


int get_total_atk() {
    int a = zh_player.atk;
    if (zh_player.eq_weapon != -1) a += get_item_by_id(zh_player.eq_weapon).value;
    
    if (has_sailor_set()) a += 10;
    if (has_phantom_set()) a += 80;   // 幻影套装强力加攻
    if (has_dragon_set()) a += 200;   // 龙血套装毁灭攻击
    
    if (has_item_in_bag(208)) a += 30;  
    if (has_item_in_bag(222)) a += 50;  
    if (has_item_in_bag(240)) a += 100; 
    if (has_item_in_bag(247)) a += 25;  
    return a;
}

int get_total_def() {
    int d = zh_player.def;
    int eq_ids[] = {zh_player.eq_head, zh_player.eq_chest, zh_player.eq_legs, zh_player.eq_shoes};
    for (int j = 0; j < 4; j++) {
        if (eq_ids[j] != -1) d += get_item_by_id(eq_ids[j]).value;
    }
    
    if (has_sailor_set()) d += 20;
    if (has_steel_set()) d += 15;
    if (has_earth_set()) d += 150;    // 大地守护套装极致防御
    if (has_phantom_set()) d += 40;
    if (has_dragon_set()) d += 120;
    
    if (has_item_in_bag(205)) d += 40;  
    if (has_item_in_bag(211)) d += 60;  
    if (has_item_in_bag(248)) d += 150; 
    return d;
}
